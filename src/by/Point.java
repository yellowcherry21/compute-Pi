package by;

import static java.lang.Math.pow;

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getDistance(Point center) {
        return Math.sqrt(pow(x-center.x,2)+pow(y-center.y,2));
    }
}

